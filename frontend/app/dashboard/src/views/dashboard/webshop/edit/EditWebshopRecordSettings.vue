<template>
    <SaveView :loading="saving" title="Vragenlijsten en gegevens" :disabled="!hasChanges" @save="save">
        <h1>
            Vragenlijsten en gegevens
        </h1>
        <p>Je kan zelf kiezen welke extra informatie je van bestellers wilt verzamelen. Dit wordt maar één keer per bestelling gevraagd.</p>

        <p v-if="webshop.meta.isTicketBased" class="info-box">
            Wil je deze info graag per ticket verzamelen (niet per bestelling), dan kan je het winkelmandje uitschakelen en het maximum aantal stuks van een ticket beperken tot 1 stuk. 
            Als je enkel keuzemenu's nodig hebt, gebruik je best de keuzemenu's bij de instellingen van een ticket, dat is iets makkelijker in gebruik.
        </p>
        <p v-else-if="webshop.meta.isRegistrations" class="info-box">
            Wil je deze info graag per ingeschreven persoon verzamelen, dan kan je het winkelmandje uitschakelen en het maximum aantal stuks van een artikel beperken tot 1 stuk.
            Als je enkel keuzemenu's nodig hebt, gebruik je best de keuzemenu's bij de instellingen van een artikel, dat is iets makkelijker in gebruik.
        </p>
            
        <STErrorsDefault :error-box="errorBox" />

        <hr>
        <h2>Ingebouwde gegevens</h2>

        <p>Bepaalde gegevens zijn ingebouwd in Stamhoofd zodat we die ook op een speciale manier kunnen verwerken. Je kan deze hier aan of uit zetten.</p>

        <STList>
            <STListItem element-name="label" :selectable="false">
                <Checkbox slot="left" :checked="true" :disabled="true" />
                <p class="style-title-list">
                    Naam
                </p>
                <p class="style-description-small">
                    Deze is altijd verplicht per bestelling.
                </p>
            </STListItem>
            <STListItem element-name="label" :selectable="false">
                <Checkbox slot="left" :checked="true" :disabled="true" />
                <p class="style-title-list">
                    E-mailadres
                </p>
                <p class="style-description-small">
                    Hierop ontvangen bestellers hun bestelbevestiging. Deze is altijd verplicht per bestelling.
                </p>
            </STListItem>

            <STListItem element-name="label" :selectable="true">
                <Checkbox slot="left" v-model="phoneEnabled" />
                <p class="style-title-list">
                    {{ $t('shared.inputs.mobile.label') }}
                </p>
            </STListItem>
        </STList>

        <hr>
        <h2>Vragenlijsten tijdens afrekenen</h2>

        <p>
            Voeg zelf vragenlijsten toe die ingevuld kunnen worden bij het plaatsen van een bestelling (na de ingebouwde gegevens). <a :href="'https://'+ $t('shared.domains.marketing') +'/docs/vragenlijsten-instellen/'" class="inline-link" target="_blank">Meer info</a>
        </p>

        <STList v-model="categories" :draggable="true">
            <RecordCategoryRow v-for="category in categories" :key="category.id" :category="category" :categories="categories" :selectable="true" :settings="editorSettings" @patch="addCategoriesPatch" />
        </STList>

        <p>
            <button class="button text" type="button" @click="addCategory">
                <span class="icon add" />
                <span>Nieuwe vragenlijst</span>
            </button>
        </p>
    </SaveView>
</template>

<script lang="ts">
import { PatchableArrayAutoEncoder } from "@simonbackx/simple-encoding";
import { ComponentWithProperties, NavigationController, NavigationMixin } from "@simonbackx/vue-app-navigation";
import { Checkbox, SaveView, STErrorsDefault, STList, STListItem } from "@stamhoofd/components";
import { UrlHelper } from "@stamhoofd/networking";
import { Checkout, WebshopType } from "@stamhoofd/structures";
import { RecordEditorSettings } from "@stamhoofd/structures";
import { PrivateWebshop, RecordCategory, WebshopMetaData } from "@stamhoofd/structures";
import { Formatter } from "@stamhoofd/utility";
import { Component, Mixins } from "vue-property-decorator";

import EditRecordCategoryQuestionsView from "../../settings/modules/members/records/EditRecordCategoryQuestionsView.vue";
import EditRecordCategoryView from "../../settings/modules/members/records/EditRecordCategoryView.vue";
import RecordCategoryRow from "../../settings/modules/members/records/RecordCategoryRow.vue";
import EditWebshopMixin from './EditWebshopMixin';


@Component({
    components: {
        SaveView,
        STErrorsDefault,
        STList,
        RecordCategoryRow,
        STListItem,
        Checkbox
    },
})
export default class EditWebshopRecordSettings extends Mixins(EditWebshopMixin) {
    mounted() {
        UrlHelper.setUrl("/webshops/" + Formatter.slug(this.webshop.meta.name) + "/settings/records")
    }

    get WebshopType() {
        return WebshopType;
    }

    get categories() {
        return this.webshop.meta.recordCategories;
    }

    set categories(recordCategories: RecordCategory[]) {
        this.addPatch(PrivateWebshop.patch({ 
            meta: WebshopMetaData.patch({
                recordCategories: recordCategories as any
            })
        }))
    }

    get phoneEnabled() {
        return this.webshop.meta.phoneEnabled;
    }

    set phoneEnabled(phoneEnabled: boolean) {
        this.addPatch(PrivateWebshop.patch({ 
            meta: WebshopMetaData.patch({
                phoneEnabled
            })
        }))
    }

    get editorSettings() {
        return new RecordEditorSettings({
            dataPermission: false,
            filterDefinitions: (categories: RecordCategory[]) => Checkout.getFilterDefinitions(this.webshop, categories),
            filterValueForAnswers: (answers) => Checkout.create({recordAnswers: answers})
        })
    }

    addCategory() {
        const category = RecordCategory.create({})

        this.present({
            components: [
                new ComponentWithProperties(NavigationController, {
                    root: new ComponentWithProperties(EditRecordCategoryView, {
                        category,
                        isNew: true,
                        filterDefinitions: this.editorSettings.filterDefinitions(this.categories),
                        saveHandler: (patch: PatchableArrayAutoEncoder<RecordCategory>, component: NavigationMixin) => {
                            this.addCategoriesPatch(patch)
                            component.show({
                                components: [
                                    new ComponentWithProperties(EditRecordCategoryQuestionsView, {
                                        categoryId: category.id,
                                        rootCategories: this.categories,
                                        settings: this.editorSettings,
                                        isNew: false,
                                        saveHandler: (patch: PatchableArrayAutoEncoder<RecordCategory>) => {
                                            this.addCategoriesPatch(patch)
                                        }
                                    })
                                ],
                                replace: 1,
                                force: true
                            })
                        }
                    })
                })
            ],
            modalDisplayStyle: "popup"
        })
    }

    addCategoriesPatch(patch: PatchableArrayAutoEncoder<RecordCategory>) {
        this.addPatch(PrivateWebshop.patch({ 
            meta: WebshopMetaData.patch({
                recordCategories: patch
            })
        }))
    }
}
</script>
