<template>
    <div class="st-view background">
        <STNavigationBar :title="pack.meta.name" :dismiss="canDismiss" :pop="canPop" />

        <main>
            <h1>
                {{ pack.meta.name }}
            </h1>

            <p>
                Alle bedragen zijn excl. BTW, tenzij anders vermeld.
            </p>

            <STErrorsDefault :error-box="errorBox" />

            <STList v-if="pack.meta.pricingType === 'PerMember'">
                <STListItem>
                    Aantal leden

                    <template slot="right">
                        {{ pack.meta.paidAmount }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.meta.unitPrice">
                    Prijs

                    <template slot="right">
                        {{ pack.meta.unitPrice | price }} / jaar / lid
                    </template>
                </STListItem>

                <STListItem v-if="pack.meta.minimumAmount">
                    Minimum bedrag per jaar

                    <template slot="right">
                        {{ pack.meta.minimumAmount * pack.meta.unitPrice | price }} 
                        ({{ pack.meta.minimumAmount }} leden)
                    </template>
                </STListItem>

                <STListItem>
                    Vanaf

                    <template slot="right">
                        {{ pack.meta.startDate | date }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.validUntil">
                    Geldig tot

                    <template slot="right">
                        {{ pack.validUntil | date }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.removeAt && pack.meta.allowRenew && !isValid">
                    Verlengbaar tot

                    <template slot="right">
                        {{ pack.removeAt | date }}
                    </template>
                </STListItem>
            </STList>

            <STList v-else-if="pack.meta.pricingType === 'PerYear'">
                <STListItem>
                    Prijs

                    <template slot="right">
                        {{ pack.meta.unitPrice | price }} / jaar
                    </template>
                </STListItem>

                <STListItem>
                    Vanaf

                    <template slot="right">
                        {{ pack.meta.startDate | date }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.validUntil">
                    Geldig tot

                    <template slot="right">
                        {{ pack.validUntil | date }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.removeAt && pack.meta.allowRenew && !isValid">
                    Verlengbaar tot

                    <template slot="right">
                        {{ pack.removeAt | date }}
                    </template>
                </STListItem>
            </STList>

            <STList v-else-if="pack.meta.pricingType === 'Fixed'">
                <STListItem v-if="pack.meta.unitPrice">
                    Prijs

                    <template slot="right">
                        {{ pack.meta.unitPrice | price }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.meta.serviceFeePercentage && pack.meta.serviceFeeFixed" class="right-description">
                    Servicekosten

                    <template slot="right">
                        {{ pack.meta.serviceFeePercentage | percentage }} +
                        {{ pack.meta.serviceFeeFixed | price }}
                    </template>
                </STListItem>

                <STListItem v-else-if="pack.meta.serviceFeePercentage" class="right-description">
                    Servicekosten per stuk/ticket

                    <template slot="right">
                        {{ pack.meta.serviceFeePercentage | percentage }}
                        <p v-if="pack.meta.serviceFeeMinimum" class="style-description-small">
                            min. {{ pack.meta.serviceFeeMinimum | price }}
                        </p>
                        <p v-if="pack.meta.serviceFeeMaximum" class="style-description-small">
                            max. {{ pack.meta.serviceFeeMaximum | price }}
                        </p>
                    </template>
                </STListItem>

                <STListItem v-else-if="pack.meta.serviceFeeFixed">
                    Servicekosten per stuk/ticket

                    <template slot="right">
                        {{ pack.meta.serviceFeeFixed | price }}
                    </template>
                </STListItem>
    
                <STListItem>
                    Vanaf

                    <template slot="right">
                        {{ pack.meta.startDate | date }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.validUntil">
                    Geldig tot

                    <template slot="right">
                        {{ pack.validUntil | date }}
                    </template>
                </STListItem>

                <STListItem v-if="pack.removeAt && pack.meta.allowRenew && !isValid">
                    Verlengbaar tot

                    <template slot="right">
                        {{ pack.removeAt | date }}
                    </template>
                </STListItem>
            </STList>
        </main>

        <STToolbar v-if="pack.meta.canDeactivate || pack.shouldHintRenew()">
            <template slot="right">
                <LoadingButton v-if="pack.meta.canDeactivate" :loading="deactivating">
                    <button class="button secundary" type="button" @click="deactivate">
                        Stopzetten
                    </button>
                </LoadingButton>

                <LoadingButton v-if="pack.shouldHintRenew()" :loading="loading">
                    <button class="button primary" type="button" @click="extend">
                        Verlengen
                    </button>
                </LoadingButton>
            </template>
        </STToolbar>
    </div>
</template>

<script lang="ts">
import { ComponentWithProperties, NavigationMixin } from "@simonbackx/vue-app-navigation";
import { BackButton, CenteredMessage, ErrorBox, LoadingButton, STErrorsDefault,STInputBox, STList, STListItem, STNavigationBar, STToolbar, Toast, Validator } from "@stamhoofd/components";
import { SessionManager } from "@stamhoofd/networking";
import { STPackage } from "@stamhoofd/structures";
import { Formatter } from "@stamhoofd/utility";
import { Component, Mixins, Prop } from "vue-property-decorator";

import PackageConfirmView from "./PackageConfirmView.vue";

@Component({
    components: {
        STNavigationBar,
        STToolbar,
        STInputBox,
        STErrorsDefault,
        BackButton,
        LoadingButton,
        STList,
        STListItem
    },
    filters: {
        price: Formatter.price,
        date: Formatter.date.bind(Formatter),
        percentage: Formatter.percentage.bind(Formatter)
    }
})
export default class PackageDetailsView extends Mixins(NavigationMixin) {
    @Prop({ required: true })
        pack: STPackage

    errorBox: ErrorBox | null = null
    validator = new Validator()

    loading = false
    deactivating = false

    extend() {
        this.show(new ComponentWithProperties(PackageConfirmView, {
            renewPackages: [this.pack]
        }))
    }

    async deactivate() {
        if (this.deactivating) {
            return
        }

        if (!await CenteredMessage.confirm("Ben je zeker dat je dit wilt stopzetten?", "Meteen stopzetten")) {
            return
        }
        this.deactivating = true

        try {
            await SessionManager.currentSession!.authenticatedServer.request({
                method: "POST",
                path: "/billing/deactivate-package/"+this.pack.id,
            })
            await SessionManager.currentSession!.fetchOrganization()
            this.pop({ force: true })
        } catch (e) {
            Toast.fromError(e).show()
        }

        this.deactivating = false
    }

    get isValid() {
        return this.pack.validUntil === null || this.pack.validUntil > new Date()
    }
  
    shouldNavigateAway() {
        // TODO
        if (this.loading) {
            return false
        }
        return true
    }
}
</script>